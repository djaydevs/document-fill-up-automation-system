    @Override
    public void run() { //scan and get data from QR code
        Result result = null;
        BufferedImage image = null;

        do {
            try {
                Thread.sleep(100);
            } catch (InterruptedException e) {
                e.printStackTrace();
            }

            try {
                if (webcam.isOpen()) {
                    if ((image = webcam.getImage()) == null) {
                        continue;  
                    }
                    LuminanceSource source = new BufferedImageLuminanceSource(image);
                    BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

                    result = new MultiFormatReader().decode(bitmap);
                }
            } catch (NotFoundException e) {
                //JOptionPane.showMessageDialog(null, e);
            }

            if (result != null) { //split data from QR code
                //result_field.setText(result.getText());
                result.getText();
                String text = result.toString();
                residentinfo = text.split(";");
                infoname = residentinfo[0];
                infoage = residentinfo[1];
                infoaddress = residentinfo[2];
                infoyearstay = residentinfo[3];
                
                choosedocpanel.setVisible(true);
                scanqrpanel.setVisible(false);
                
            }
        } while(true);
    }

